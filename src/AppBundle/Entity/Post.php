    }

    /**
     * @return string
     */
    protected function getUploadRootDir()
    {
        // the absolute directory path where uploaded
        // documents should be saved
        return __DIR__.'/../../../web/'.$this->getUploadDir();
    }

    /**
     * @return string
     */
    protected function getUploadDir()
    {
        // get rid of the __DIR__ so it doesn't screw up
        // when displaying uploaded doc/image in the view.
        return 'uploads/' . $this->getPath();
    }

    /**
     * Helper to get a unique clean file name
     */
    private function uniqueCleanFilename()
    {
        $name = $this->getFile()->getClientOriginalName();

        // remove extension
        $name = pathinfo($name, PATHINFO_FILENAME);

        // clean name
        $options = array(
            'delimiter' => '_',
            'transliterate' => true,
        );
        $name = $this->sanitize($name, $options);

        // ensure it is unique
        $i = 0;
        $try = $this->getUploadRootDir() . '/' . $name . '.' . $this->getFile()->guessExtension();
        while (file_exists($try)) {
            $i++;
            $try = $this->getUploadRootDir() . '/' . $name . '_' . $i . '.' . $this->getFile()->guessExtension();
        }

        if ($i > 0) {
            $name = $name . '_' . $i;
        }

        // add extension back, depending on mime type
        $name = $name . '.' . $this->getFile()->guessExtension();

        return $name;
    }

